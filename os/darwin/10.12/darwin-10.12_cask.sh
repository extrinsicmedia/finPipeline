### Cask Installs

# Install Cask
brew install caskroom/cask/brew-cask

# Install applications
brew cask install slack # Slack communication tool
brew cask install blender # open source 3d
brew cask install cyberduck # FTP program
brew cask install djv # DJViewer open source sequence viewer
brew cask install gimp # open source image editor
brew cask install libreoffice # open source office suite
brew cask install meshlab # open source 3d conversion
brew cask install osxfuse # used with veracrypt
brew cask install vagrant # used with virtualization software
brew cask install veracrypt # encrypted disk software
brew cask install virtualbox # os virtualization software
brew cask install vlc # open source video player
brew cask install xquartz # xquartz driver
